export function criarItemArquivos(
  itemCatalogo,
) {
  return {
    ...itemCatalogo,
    id:
      `item-${Date.now()}-${Math.random()
        .toString(36)
        .slice(2, 8)}`,
    quantidade:
      Number(
        itemCatalogo?.quantidade,
      ) || 1,
    ativo:
      itemCatalogo?.ativo !==
      false,
  };
}

